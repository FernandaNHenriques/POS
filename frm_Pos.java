    private void bt_quantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_quantidadeActionPerformed
        // TODO add your handling code here:
        if(list_descricao.getSelectedIndex()<0){
           JOptionPane.showMessageDialog(null,
                    "Deve selecionar um item.", "Quantidade",
                    JOptionPane.ERROR_MESSAGE); 
            return;
        }
        int qtd=0;
        try{
            qtd=Integer.parseInt(JOptionPane.showInputDialog(this,"Digite a quantidade: "));
            if(qtd<=0){
                JOptionPane.showMessageDialog(this,"Quantidade Inválida",
                        "POS",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Verifique os Dados",
                    "POS", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int posicao=list_descricao.getSelectedIndex();
        list_qtd.setSelectedIndex(posicao);
        int new_qtd=list_qtd.getSelectedValue()+qtd;
        quantidade.setElementAt(new_qtd,posicao);
        
        list_preco.setSelectedIndex(posicao);
        float prc=list_preco.getSelectedValue();
        list_total.setSelectedIndex(posicao);
        total.setElementAt(Moeda.format(new_qtd*prc), posicao);
        
        total_pedido+=(prc*qtd);
        lbl_total.setText(Moeda.format(total_pedido));
        
        list_descricao.setSelectedIndex(list_qtd.getSelectedIndex());
    }//GEN-LAST:event_bt_quantidadeActionPerformed

    private void bt_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_removerActionPerformed
        // TODO add your handling code here:
        if(list_descricao.getSelectedIndex()<0){
           JOptionPane.showMessageDialog(null,
                    "Deve selecionar um item.", "Quantidade",
                    JOptionPane.ERROR_MESSAGE); 
            return;
        }
        int qtd=0;
        try{
            qtd=Integer.parseInt(JOptionPane.showInputDialog(this,"Digite a quantidade: "));
            if(qtd<=0 || qtd>list_qtd.getSelectedValue()){
                JOptionPane.showMessageDialog(this,"Quantidade Inválida.",
                        "POS",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Verifique os Dados",
                    "POS", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int posicao=list_descricao.getSelectedIndex();
        list_qtd.setSelectedIndex(posicao);
        int new_qtd=list_qtd.getSelectedValue()-qtd;
        quantidade.setElementAt(new_qtd,posicao);
        
        list_preco.setSelectedIndex(posicao);
        float prc=list_preco.getSelectedValue();
        list_total.setSelectedIndex(posicao);
        total.setElementAt(Moeda.format(new_qtd*prc), posicao);
        
        total_pedido-=(prc*qtd);
        lbl_total.setText(Moeda.format(total_pedido ));
       
        
        list_descricao.setSelectedIndex(list_qtd.getSelectedIndex());
        
        if (new_qtd==0){
            descricao.remove(list_descricao.getSelectedIndex());
            quantidade.remove(list_qtd.getSelectedIndex());
            preco.remove(list_preco.getSelectedIndex());
            total.remove(list_total.getSelectedIndex());
        }
    }//GEN-LAST:event_bt_removerActionPerformed

    private void bt_pagamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pagamentosActionPerformed
        if (list_descricao.getModel().getSize()==0){
            JOptionPane.showMessageDialog(null,
                    "Não existem itens adicionados.", "Pagamento",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        float recebido=0;
        try{
            recebido=Float.parseFloat(JOptionPane.showInputDialog(this,
                    "Digite o valor entregue a pagamento:"));
            if(recebido<total_pedido){
                JOptionPane.showMessageDialog(this,
                        "Valor Insuficiente", "POS",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            
        }
        catch(NumberFormatException e){
             JOptionPane.showMessageDialog(this,
                        "Dígito Inválido", "POS",
                        JOptionPane.ERROR_MESSAGE);
                return; 
        }
        lbl_numerario.setText(Moeda.format(recebido));
        lbl_troco.setText(Moeda.format(recebido-total_pedido));
        
        Desabilita_Botoes();
        Desabilitar_Paineis();
        
        bt_imprimir.setEnabled(true);
        
    }//GEN-LAST:event_bt_pagamentosActionPerformed

    private void bt_imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_imprimirActionPerformed
        PrinterJob J=PrinterJob.getPrinterJob();
        J.setPrintable(new Imprimir());
        J.printDialog();
        try{
            J.print();
                        
        }
        
        catch(PrinterException e){
            JOptionPane.showMessageDialog(this,"Erro ao Imprimir",
                    "POS",JOptionPane.ERROR_MESSAGE); 
        }
        
         // guardar na base de dados
        LigacaoMySql bd=new LigacaoMySql();
        try{
            Connection Cn=bd.CriarLigacaoMySql();
            Statement st=Cn.createStatement();
            st.executeUpdate("insert into pedidos (id_pedidos,valor) " +
            "values ('"+lbl_pedido.getText()+"','"+total_pedido+"')");
            
            Clear_All();
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(this, 
                    "Erro ao guardar o registo","POS",
                     JOptionPane.ERROR_MESSAGE); 
    }//GEN-LAST:event_bt_imprimirActionPerformed
    }
    private void Clear_All(){
            total_pedido=0;
            descricao.clear();
            quantidade.clear();
            preco.clear();
            total.clear();
            lbl_total.setText("");
            lbl_numerario.setText("");
            lbl_troco.setText("");
            lbl_pedido.setText("");
            
            bt_novo.setText("Novo");
            bt_imprimir.setEnabled(false);
    }
    
    private void bt_novoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_novoActionPerformed
        // TODO add your handling code here:
        if (bt_novo.getText().equals("Novo")){
            
        GregorianCalendar C=new GregorianCalendar();
        int dia=C.get(Calendar.DAY_OF_MONTH);
        int mes=C.get(Calendar.MONTH);
        int ano=C.get(Calendar.YEAR);
        
        int hora=C.get(Calendar.HOUR_OF_DAY);
        int minuto=C.get(Calendar.MINUTE);
        int segundo=C.get(Calendar.SECOND);
        
        lbl_pedido.setText(Integer.toString(dia)+
                           Integer.toString(mes)+
                           Integer.toString(ano)+
                           Integer.toString(hora)+
                           Integer.toString(minuto)+
                           Integer.toString(segundo));
        bt_novo.setText("Cancelar");
        Habilitar_Paineis();
        }
        else{
            bt_novo.setText("Novo");
            lbl_pedido.setText("");
            Desabilitar_Paineis();
            Clear_All();
        }
    }//GEN-LAST:event_bt_novoActionPerformed

