    private void AdicionarProduto(String NomeProduto, float PrecoProduto) {
        try{
            int posicao=list_descricao.getNextMatch(NomeProduto,
                    0, Position.Bias.Forward);
                    list_qtd.setSelectedIndex(posicao);
                    int qtd = list_qtd.getSelectedValue()+1;
                    quantidade.setElementAt(qtd, posicao);
                    
                    list_total.setSelectedIndex(posicao);
                    total.setElementAt(qtd*PrecoProduto, posicao);
                    
                    total_pedido+=PrecoProduto;
                    lbl_total.setText(Moeda.format(total_pedido));
                    
                    list_descricao.setSelectedIndex(posicao);
                    list_preco.setSelectedIndex(posicao);
                    return;
            }
        catch(Exception e){
            
        descricao.addElement(NomeProduto);
        list_descricao.setModel(descricao);
        
        quantidade.addElement(1);
        list_qtd.setModel(quantidade);
        
        preco.addElement(PrecoProduto);
        list_preco.setModel(preco);
        
        total.addElement(PrecoProduto);
        list_total.setModel(total);
        
        total_pedido+=PrecoProduto;
        lbl_total.setText(Moeda.format(total_pedido));
        Habilita_Botoes();
        }
    }
    
    
